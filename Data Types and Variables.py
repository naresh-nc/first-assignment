# 1. Creating variables
my_integer = 80
my_float = 53.14
my_string = "Hello World!"
my_boolean = True

# 2. Printing Variable Types:
print("Integer type:", type(my_integer))
print("Float type:", type(my_float))
print("String type:", type(my_string))
print("Boolean type:", type(my_boolean))

#3. Performing Arithmetic Operations:

r_addition = my_integer + my_float
r_subtraction = my_integer - my_float
r_multiplication = my_integer * my_float
r_division = my_integer / my_float

print("Addition:", r_addition)
print("Subtraction:", r_subtraction)
print("Multiplication:", r_multiplication)
print("Division:", r_division)